// Arrays ======================
// Array Literal [] or empty array

// Arrays are object we can use key value pairs.

let selectedColors = ["red", "blue"];

selectedColors[2] = "green"; // new element for index entry 2 is = 'green'.

selectedColors[3] = 1; // new element for index entry 2 is = 1.
console.log(selectedColors);
console.log(selectedColors[0]);
console.log(selectedColors[1]);
console.log(selectedColors[2]);
console.log(selectedColors[3]);

console.log(selectedColors.length); // length property.

