// While loop
// You will have to declare variable before using while loop

console.log("While loop");
console.log("-------------------");

let i = 0;
console.log("Check the odd number");
while (i <= 5) {
  if (i % 2 !== 0) console.log(i);
  i++;
}

// Check even number in reverse order
console.log("\nCheck odd number in reverse order");
while (i >= 1) {
  if (i % 2 !== 0) console.log(i);
  i--;
}
