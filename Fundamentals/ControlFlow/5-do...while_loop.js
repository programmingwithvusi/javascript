// Do..While loop
// You will have to declare variable before using do-while loop
// The statement is executed at least once before the condition evaluated.

console.log("do...while loop");
console.log("-------------------");

let j = 0;
console.log("");
do {
  if (j % 2 !== 0) console.log(j);
  j++;
} while (j <= 5);
