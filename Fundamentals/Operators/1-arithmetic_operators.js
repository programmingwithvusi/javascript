// Arithmetic Operators =========================================
let x = 10;
let y = 3;

console.log("Addition operator: ", x + y); // Addition operator  ->{output: 13}
console.log("Subtraction operator: ", x - y); // Subtraction operator -> {output: 7}
console.log("Multiplication operator: ", x * y); // Multiplication operator  -> {output: 30}
console.log("Division operator: ", x / y); // Division operator ->  {output: 3.3333333333333335}
console.log("Remainder operator: ", x % y); // Remainder operator -> {output: 1}
console.log("Exponent operator: ", x ** y); // Exponent operator -> {output: 1000}

// Increment (++)
console.log(x++); // pre-increments. -> {output: 10}
console.log(x); // -> {output: 11}

console.log(++x); // post-increments. -> {output: 12}
console.log(x); // -> {output: 12}

// Decrements (--)
console.log(--x); // pre-decrements. -> {output: 11}
console.log(x); // -> {output: 11}

console.log(x--); // post-decrements. -> {output: 11}
console.log(x); // -> {output: 10}
